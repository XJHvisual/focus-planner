def _pid_alive(pid):
    """检测指定 PID 的进程是否存活（Windows）"""
    try:
        import ctypes
        kernel32 = ctypes.windll.kernel32
        SYNCHRONIZE = 0x00100000
        h = kernel32.OpenProcess(SYNCHRONIZE, 0, pid)
        if h == 0:
            return False
        # WaitForSingleObject(h, 0) → WAIT_TIMEOUT(0x102) 表示进程仍在运行
        ret = kernel32.WaitForSingleObject(h, 0)
        kernel32.CloseHandle(h)
        return ret == 0x102
    except Exception:
        # 降级：os.kill(pid, 0) 在 Windows 上不可靠，但聊胜于无
        try:
            os.kill(pid, 0)
            return True
        except OSError:
            return False


def is_another_instance_running():
    """PID 文件锁：锁文件存在且 PID 仍存活 → 已有实例；
       进程已死或锁损坏 → 自动清理，返回 False"""
    if os.path.exists(_LOCK_FILE):
        try:
            with open(_LOCK_FILE, "r") as f:
                old_pid = int(f.read().strip())
            if _pid_alive(old_pid):
                return True
            # 旧进程已死，清理残留锁
            os.remove(_LOCK_FILE)
        except (ValueError, OSError):
            try:
                os.remove(_LOCK_FILE)
            except OSError:
                pass
    # 写入当前 PID
    try:
        with open(_LOCK_FILE, "w") as f:
            f.write(str(os.getpid()))
        return False
    except OSError:
        # 极端情况：无法写入锁文件，放行
        return False


def _cleanup_lock():
    """退出时清理自己的锁文件"""
    try:
        if os.path.exists(_LOCK_FILE):
            with open(_LOCK_FILE, "r") as f:
                pid = int(f.read().strip())
            if pid == os.getpid():
                os.remove(_LOCK_FILE)
    except (OSError, ValueError):
        pass

atexit.register(_cleanup_lock)
